package javaday1_project;

public class Loops {

	public static void main(String[] args) {
		 int n=10;

		  System.out.println("Ascending Order:");

		  for(int i=0;i<=n;i++) {

			  System.out.println(i);

		  }

		  //descending order

		     int m=10;

			  System.out.println("Descending Order:");

			  for(int i=1;m>=i;m--) {

				  System.out.println(m);

		  }

			  

	}

}
